package assignment;

public class Forupper {

	public static void main(String[] args) {
		System.out.println("*****Upper Case Alphabets******");
		for(char ch='A';ch<='Z';ch++) {
			System.out.print(ch+" ");
		}
		System.out.println("\n*****Lower Case Alphabets******");
		for(char ch='a';ch<='z';ch++) {
			System.out.print(ch+" ");
		}

	}

}
