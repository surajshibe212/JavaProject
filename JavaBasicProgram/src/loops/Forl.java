package loops;

public class Forl {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			System.out.print(i+" ");
		}System.out.println("\n*********************");
        for (int i=10;i>=0;i--) {
        	System.out.print(i+" ");
        }System.out.println("\n*********************");
        for(char c1='a';c1<='z';c1++) {
        	System.out.print(c1+" ");
        }
	}

}
